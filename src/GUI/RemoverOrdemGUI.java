    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;
        int click = e.getClickCount();
        int index = jTable1.getSelectedRow(); 
        OrdemServico os = this.respostaOrdem.get(index);
        if(click == 2)
        {
            String mensagem = "Deseja remover esta OS?\nNúmero: " + os.getNumero() + "\nCliente: " + os.getCliente().getNome()+ "\nFuncionário: " + os.getFuncionario().getNome();
            int resposta = JOptionPane.showConfirmDialog(this, mensagem, "Remover", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(resposta == JOptionPane.YES_OPTION)
            {
                try
                {
                    OrdemServico ordemServico = new OrdemServico();
                    ordemServico.setNumero(os.getNumero());
                    NegocioOrdemServico negocioOrdem = new NegocioOrdemServico();
                    negocioOrdem.removerOrdemServico(ordemServico);
                    JOptionPane.showMessageDialog(this, "OS Removida", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    ListarOrdem();
                }
                catch (Exception ex)
                {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }           
        }
    }//GEN-LAST:event_jTable1MouseClicked

