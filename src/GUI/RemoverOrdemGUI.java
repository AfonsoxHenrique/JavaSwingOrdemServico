    public void ListarOrdem()
    {
        try
        {
            SimpleDateFormat formato2 = new SimpleDateFormat("dd/MM/yyyy");
            OrdemServico ordemServico = new OrdemServico();
            NegocioOrdemServico negocioOrdem = new NegocioOrdemServico();
            this.respostaOrdem = negocioOrdem.listarOrdemServicoAberta(ordemServico);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Cliente", "CPF Cliente", "Funcionário", "Data de cadastro", "Problema"});
            
            for (OrdemServico os : this.respostaOrdem) 
            {
                modelo.addRow(new Object[]{os.getCliente().getNome(), os.getCliente().getCpf(), os.getFuncionario().getNome(), formato2.format(os.getDataCadastro()), os.getClassificacaoProblema().getNomeClassProblema()});
            }
            jTable1.setModel(modelo);
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
