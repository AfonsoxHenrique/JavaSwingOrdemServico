    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;
        
        int click = e.getClickCount();
        int index = jTable1.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        String valor = modelo.getValueAt(index, 0).toString();
        String valor1 = modelo.getValueAt(index, 1).toString();
        String valor2 = modelo.getValueAt(index, 2).toString();
        String valor3 = modelo.getValueAt(index, 3).toString();
        String valor4 = modelo.getValueAt(index, 4).toString();
        
        if (click == 2)
        {
            jButtonEditar.setVisible(true);
            jTextFieldMatricula.setText(valor);
            jTextFieldCPF.setText(valor2);
            jTextFieldNomeNovo.setText(valor1);
            jTextFieldRG.setText(valor3);
            jTextFieldSalarioNovo.setText(valor4);
        }
    }//GEN-LAST:event_jTable1MouseClicked

