    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed

        if(jTextFieldMatricula.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Escolha o funcionário", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try 
        {
            Funcionario funcionario = new Funcionario();    
                        
            funcionario.setSalario(Double.parseDouble(jTextFieldSalarioNovo.getText()));
            funcionario.setNome(jTextFieldNomeNovo.getText());
            funcionario.setCpf(jTextFieldCPF.getText());
            funcionario.setRg(jTextFieldRG.getText());
            funcionario.setMatricula(Integer.parseInt(jTextFieldMatricula.getText()));            
            NegocioFuncionario negocioFuncionario = new NegocioFuncionario();
            negocioFuncionario.editarFuncionario(funcionario);
            
            listarFuncinario();
            jTextFieldNomeNovo.setText("");
            jTextFieldCPF.setText("");
            jTextFieldRG.setText("");
            jTextFieldSalarioNovo.setText("");
            jTextFieldMatricula.setText("");
            JOptionPane.showMessageDialog(this, "Funcionário editado", "Aviso", JOptionPane.INFORMATION_MESSAGE);                      
        } 
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

