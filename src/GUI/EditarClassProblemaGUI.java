    public void ListarClassProblema()
    {
        try
        {
            ClassificacaoProblema classProblema = new ClassificacaoProblema();
            NegocioClassificacaoProblema negocioClass = new NegocioClassificacaoProblema();
            this.respostaClass = negocioClass.listarClassProblema(classProblema);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Código", "Nome da Classificação", "Descrição"});
            
            for (ClassificacaoProblema cl : this.respostaClass) 
            {
                modelo.addRow(new Object[]{cl.getCod_ClassProblema(), cl.getNomeClassProblema(), cl.getDescricao()});
            }
            jTable1.setModel(modelo);
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }
