    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        
        try 
        {   
            if(jTextFieldCódigoAntigoClass.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Selecione o problema desejado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            ClassificacaoProblema classificacaoProblema = new ClassificacaoProblema();
            classificacaoProblema.setCod_ClassProblema(Integer.parseInt(jTextFieldCódigoAntigoClass.getText()));
            classificacaoProblema.setDescricao(jTextAreaDescricaoNovaClass.getText());
            classificacaoProblema.setNomeClassProblema(jTextFieldNomeNovoClass.getText());
            
            NegocioClassificacaoProblema negocioClass = new NegocioClassificacaoProblema();
            negocioClass.editarClassProblema(classificacaoProblema);
            ListarClassProblema();
            jTextAreaDescricaoNovaClass.setText("");
            jTextFieldCódigoAntigoClass.setText("");
            jTextFieldNomeNovoClass.setText("");
            JOptionPane.showMessageDialog(this, "Classificação editada com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (Exception e)  
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

