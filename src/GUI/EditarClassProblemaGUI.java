    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;       
        int click = e.getClickCount();
        int index = jTable1.getSelectedRow();
        if (click == 2)
        {
            try
            {
                ClassificacaoProblema classProblema = this.respostaClass.get(index);
                jTextFieldCódigoAntigoClass.setText(String.valueOf(classProblema.getCod_ClassProblema()));
                jTextFieldNomeNovoClass.setText(classProblema.getNomeClassProblema());
                jTextAreaDescricaoNovaClass.setText(classProblema.getDescricao());
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
            }
        }       
    }//GEN-LAST:event_jTable1MouseClicked

