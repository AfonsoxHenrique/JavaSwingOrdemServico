    public void ListarClassProblema()
    {
        try
        {
            ClassificacaoProblema classProblema = new ClassificacaoProblema();
            DadosClassificacaoProblema dadosClass = new DadosClassificacaoProblema();
            this.respostaClass = dadosClass.listarClassProblema(classProblema);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Código", "Descrição", "Nome"});
            for (ClassificacaoProblema cl : this.respostaClass) 
            {
                modelo.addRow(new Object[]{cl.getCod_ClassProblema(), cl.getDescricao(), cl.getNomeClassProblema()});
            }
            jTable1.setModel(modelo);
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
