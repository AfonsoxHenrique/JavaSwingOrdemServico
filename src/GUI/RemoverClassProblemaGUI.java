    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;       
        int index = jTable1.getSelectedRow();
        int click = e.getClickCount();       
        if(click == 2)
        {
            try
            {
                ClassificacaoProblema classProblema = this.respostaClass.get(index);
                String mensagem = "Deseja remover esta classificação?\nCódigo:" + classProblema.getCod_ClassProblema()+ "\nNome:" + classProblema.getNomeClassProblema();
                int resultado = JOptionPane.showConfirmDialog(this, mensagem, "Remover classificação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if(resultado == JOptionPane.YES_OPTION)
                {
                    NegocioClassificacaoProblema negocioClass = new NegocioClassificacaoProblema();
                    negocioClass.excluirClassProblema(classProblema);
                    JOptionPane.showMessageDialog(this, "Classificação excluída", "Remover classificação", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Remover classificação", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

